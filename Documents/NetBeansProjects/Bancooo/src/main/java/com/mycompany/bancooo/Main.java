    private void btnAtenderTurnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtenderTurnoActionPerformed
        
        if (cola.isEmpty()) {
            JOptionPane.showMessageDialog(null, "No hay cliente pendiente");
            lblTurnoActual.setText("");
            lblClienteActual.setText("");
        } else {
            // Obtener el turno de la cola
            Turno turno = cola.dequeue();

            // Sacar datos del turno
            String numeroTurno = turno.getNumero();
            String clienteTurno = turno.getCliente();

            // Mostrar en las etiquetas
            lblTurnoActual.setText(numeroTurno);
            lblClienteActual.setText(clienteTurno);

            // Actualizar lista
            setListPaint();
        }
        
    }//GEN-LAST:event_btnAtenderTurnoActionPerformed

    private void btnAgregarTurnoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarTurnoActionPerformed
        
        // Abrir la ventana para agregar un turno
        AddTurno nuevaVentana = new AddTurno(this, true);
        nuevaVentana.setVisible(true);

        // Obtener el turno de la ventana
        Turno turnoNuevo = nuevaVentana.getTurno();

        // Si no es nulo, lo agregamos a la cola
        if (turnoNuevo != null) {
            cola.enqueue(turnoNuevo);

            // Actualizar la lista de turnos
            setListPaint();
        }
        
    }//GEN-LAST:event_btnAgregarTurnoActionPerformed

