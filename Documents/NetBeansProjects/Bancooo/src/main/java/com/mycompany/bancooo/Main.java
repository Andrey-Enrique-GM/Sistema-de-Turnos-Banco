    private void setListPaint() {

        DefaultListModel modelo = new DefaultListModel();

        if (!cola.isEmpty()) {
            Nodo aux = cola.inicio;
            while (aux != null) {
                Turno t = aux.valor;
                String texto = t.getNumero() + " - " + t.getCliente();
                modelo.addElement(texto);
                aux = aux.siguiente;
            }
        }

        lstListaTurnos.setModel(modelo);
        
    }

